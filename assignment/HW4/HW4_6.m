clear all clc;
h = 0.1;
t(1) = 1;
n = (2 - 1)/h;
y1(1) = 2;
y2(1) = 8;
y3(1) = 6;
f = @(t,y1,y2,y3)[[0, 1, 0];[0, 0, 1];[-2/t^3, 2/t^2, -1/t]] * [y1; y2; y3] + [0; 0; 8 - 2/t^3];
for i = 2 : n + 1
   t(i) = t(i - 1) + h;
   k1 = f(t(i - 1), y1(i - 1), y2(i - 1), y3(i - 1));
   k2 = f(t(i - 1) + h/2, y1(i - 1) + h/2 * k1(1), y2(i - 1) + h/2 * k1(2), y3(i - 1) + h/2 * k1(3));
   k3 = f(t(i - 1) + h/2, y1(i - 1) + h/2 * k2(1), y2(i - 1) + h/2 * k2(2), y3(i - 1) + h/2 * k2(3));
   k4 = f(t(i - 1) + h, y1(i - 1) + h * k3(1), y2(i - 1) + h * k3(2), y3(i - 1) + h * k3(3));
   y1(i) = y1(i - 1) + h/6 * (k1(1) + k2(1) + k3(1) + k4(1));
   y2(i) = y2(i - 1) + h/6 * (k1(2) + k2(2) + k3(2) + k4(2));
   y3(i) = y3(i - 1) + h/6 * (k1(3) + k2(3) + k3(3) + k4(3));
end
